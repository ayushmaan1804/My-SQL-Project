 -- GRADE CALCULATION DYNAMIC
SELECT
S.NAME,
C.COURSE_NAME,
M.MARKS,
CASE
 WHEN M.MARKS >= 90 THEN 'A+'
 WHEN M.mARKS >= 75 THEN 'A'
 WHEN M.MARKS >= 60 THEN 'B'
 WHEN M.MARKS >= 50 THEN 'C'
 ELSE 'F'
END AS GRADE
FROM MARKS m
JOIN STUDENT S ON M.STUDENT_ID = S.STUDENT_ID
JOIN COURSE C ON M.COURSE_ID = C.COURSE_ID;

-- FEE STATUS DYNAMIC
SELECT 
S.NAME,
F.TOTAL_FEE,
F.PAID_AMOUNT,
(F.TOTAL_FEE - F.PAID_AMOUNT) AS DUE_AMOUNT,
CASE
 WHEN(F.TOTAL_FEE - F.PAID_AMOUNT) = 0 THEN 'PAID'
 ELSE 'PENDING'
END AS STATUS 
FROM FEES f
JOIN STUDENT S ON F.STUDENT_ID = S.STUDENT_ID;

-- ATTENDANCE PERCENTAGE

SELECT 
S.NAME,
C.COURSE_NAME,
ROUND(SUM(A.STATUS='PRESENT')*100/COUNT(*),2) AS ATTENDANCE_PERCENTAGE
FROM ATTENDANCE A 
JOIN STUDENT S ON A.STUDENT_ID = S.STUDENT_ID
JOIN COURSE C ON A.COURSE_ID = C.COURSE_ID
group by S.NAME,C.COURSE_NAME;

-- TOPPER PER COURSE
SELECT
C.COURSE_NAME,
S.NAME,
M.MARKS
FROM MARKS M 
JOIN STUDENT S ON M.STUDENT_ID = S.STUDENT_ID
JOIN COURSE C ON M.COURSE_ID = C.COURSE_ID
WHERE (C.COURSE_ID,M.MARKS) IN (
 SELECT COURSE_ID,MAX(MARKS)
 FROM MARKS
 GROUP BY COURSE_ID);
 
 -- LOW ATTENDENCE STUDENTS
 SELECT * FROM (
  SELECT
  S.NAME,
  ROUND(SUM(A.STATUS='PRESENT')*100/COUNT(*),2) AS PERCENTAGE
  FROM ATTENDANCE A 
  JOIN STUDENT S ON A.STUDENT_ID = S.STUDENT_ID
  group by S.NAME)
  AS TEMP
  WHERE PERCENTAGE < 75;
  
  -- FEE DEFAULTER
SELECT
  S.NAME,
  (F.TOTAL_FEE - F.PAID_AMOUNT) AS DUE_AMOUNT
  FROM FEES f
  JOIN STUDENT S ON F.STUDENT_ID = S.STUDENT_ID
  WHERE (F.TOTAL_FEE - F.PAID_AMOUNT) > 0;
  
  -- REPORT CARD VIEW 
CREATE VIEW REPORT_CARD AS
 SELECT
 S.NAME,
 C.COURSE_NAME,
 M.MARKS,
  CASE
  WHEN M.MARKS >= 90 THEN 'A+'
  WHEN M.MARKS >= 75 THEN 'A'
  WHEN M.MARKS >= 60 THEN 'B'
  WHEN M.MARKS >= 50 THEN 'C'
  ELSE 'F'
END AS GRADE
FROM MARKS m
JOIN STUDENT S ON M.STUDENT_ID = S.STUDENT_ID
JOIN COURSE C ON M.COURSE_ID = C.COURSE_ID;
SELECT * FROM REPORT_CARD;

