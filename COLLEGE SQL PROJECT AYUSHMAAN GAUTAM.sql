CREATE DATABASE COLLEGE_01;
USE COLLEGE_01;

CREATE TABLE DEPARTMENT (
DEPT_ID INT PRIMARY KEY auto_increment,
DEPT_NAME VARCHAR(100) UNIQUE NOT NULL); -- DEPARTMENT TABLE

CREATE TABLE STUDENT (
STUDENT_ID INT PRIMARY KEY auto_increment,
NAME VARCHAR (100),
EMAIL VARCHAR(100) UNIQUE,
PHONE VARCHAR(15),
GENDER ENUM ('MALE','FEMALE','OTHER'),
DOB DATE,
DEPT_ID INT,
ADMISSION_DATE DATE DEFAULT (CURRENT_DATE),
FOREIGN KEY(DEPT_ID) references DEPARTMENT(DEPT_ID)); -- STUDENT TABLE

CREATE TABLE FACULTY(
FACULTY_ID INT PRIMARY KEY auto_increment,
NAME VARCHAR(100),
EMAIL VARCHAR(100),
SALARY DECIMAL(10,2),
DEPT_ID INT,
FOREIGN KEY (DEPT_ID) references DEPARTMENT(DEPT_ID));  -- FACULTY TABLE

CREATE TABLE COURSE (
COURSE_ID INT PRIMARY KEY auto_increment,
COURSE_NAME VARCHAR(100),
CREDITS INT,
DEPT_ID INT,
FACULTY_ID INT,
FOREIGN KEY (DEPT_ID) references DEPARTMENT(DEPT_ID),
foreign key (FACULTY_ID) references FACULTY(FACULTY_ID)); -- COURSE TABLE

CREATE TABLE ENROLLMENT (
ENROLL_ID INT PRIMARY KEY auto_increment,
STUDENT_ID INT,
COURSE_ID INT,
ENROLL_DATE DATE DEFAULT (CURRENT_DATE),
UNIQUE(STUDENT_ID,COURSE_ID),
foreign key (STUDENT_ID) references STUDENT(STUDENT_ID),
foreign key (COURSE_ID) references COURSE(COURSE_ID));  -- ENROLLMENT TABLE

CREATE TABLE ATTENDANCE(
ATTENDANCE_ID INT PRIMARY KEY auto_increment,
STUDENT_ID INT,
COURSE_ID INT,
DATE DATE,
STATUS ENUM ('PRESENT','ABSENT'),
foreign key (STUDENT_ID) references STUDENT(STUDENT_ID),
foreign key (COURSE_ID) references COURSE(COURSE_ID));  -- ATTENDANCE TABLE
 
CREATE TABLE MARKS (
MARKS_ID INT PRIMARY KEY auto_increment,
STUDENT_ID INT,
COURSE_ID INT,
MARKS INT CHECK (MARKS BETWEEN 0 AND 100),
foreign key (STUDENT_ID) references STUDENT(STUDENT_ID),
foreign key (COURSE_ID) references COURSE (COURSE_ID));  -- MARKS TABLE

CREATE TABLE FEES (
FEE_ID INT PRIMARY KEY auto_increment,
STUDENT_ID INT,
TOTAL_FEE decimal(10,2),
PAID_AMOUNT decimal(10,2),
foreign key (STUDENT_ID) references STUDENT(STUDENT_ID)); -- FEES TABLE

